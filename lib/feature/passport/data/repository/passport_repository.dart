import 'package:dio/dio.dart';
import 'package:dartz/dartz.dart';

import '../../../../core/utils/failures.dart';
import '../../../../core/network/api_service.dart';
import '../model/passport_model.dart';

class PassportRepository {
  final ApiService _apiService;

  PassportRepository(this._apiService);

  Future<Either<Failure, List<PassportSearchModel>>> search({
    required String name,
    required String passportNumber,
  }) async {
    try {
      final httpResponse =
          await _apiService.searchPassport(name, passportNumber);
      return Right(httpResponse.data);
    } on DioException catch (e) {
      return Left(ServerFailure.fromDioError(e));
    } catch (e) {
      return Left(ServerFailure(e.toString()));
    }
  }

  Future<Either<Failure, PassportModel>> findById(int id) async {
    try {
      final httpResponse = await _apiService.findPassportById(id);
      return Right(httpResponse.data);
    } on DioException catch (e) {
      return Left(ServerFailure.fromDioError(e));
    } catch (e) {
      return Left(ServerFailure(e.toString()));
    }
  }

  Future<Either<Failure, PassportModel>> save(PassportModel model) async {
    try {
      final httpResponse = await _apiService.savePassport(model);
      return Right(httpResponse.data);
    } on DioException catch (e) {
      return Left(ServerFailure.fromDioError(e));
    } catch (e) {
      return Left(ServerFailure(e.toString()));
    }
  }

  Future<Either<Failure, PassportModel>> update(
      int id, PassportModel model) async {
    try {
      final httpResponse = await _apiService.updatePassport(id, model);
      return Right(httpResponse.data);
    } on DioException catch (e) {
      return Left(ServerFailure.fromDioError(e));
    } catch (e) {
      return Left(ServerFailure(e.toString()));
    }
  }

  Future<Either<Failure, void>> delete(int id) async {
    try {
      final httpResponse = await _apiService.deletePassport(id);
      return Right(httpResponse.data);
    } on DioException catch (e) {
      return Left(ServerFailure.fromDioError(e));
    } catch (e) {
      return Left(ServerFailure(e.toString()));
    }
  }
}
